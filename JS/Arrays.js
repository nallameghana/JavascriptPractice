//Array initialization

var a = [12, "hi", true, 11.5]

console.log(a)

console.log(a[1])           //access an element 

a[1] = "hello"        //Replace an element 

console.log(a)

console.log("The length of array is ", a.length)    //Length of an array

a.pop()  // Returns and removes the last element in an array

console.log(a)

a.push('BMW')  //adds the element to the end of array

console.log(a)

a.shift()  //Returns and removes the first element in the array

console.log(a)

a.unshift(100)  // Adds the element at the start of an array

console.log(a)

